/* global console, process */

import { createHash } from 'node:crypto';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const IMPORTER_SOURCE_FILES = [
  'src/midi-file-import.ts',
  'src/musescore-file-import.ts',
  'src/gp-import.ts',
];

const outputPath = path.join(repoRoot, 'src', 'generated', 'importer-version.ts');

function toPosixPath(value) {
  return value.split(path.sep).join('/');
}

async function buildImporterVersion() {
  const hash = createHash('sha256');
  for (const relativePath of IMPORTER_SOURCE_FILES) {
    const absolutePath = path.join(repoRoot, relativePath);
    const content = await readFile(absolutePath, 'utf8');
    hash.update(`FILE:${toPosixPath(relativePath)}\n`);
    hash.update(content.replace(/\r\n/g, '\n'));
    hash.update('\n');
  }
  return hash.digest('hex').slice(0, 12);
}

function buildOutput(version) {
  return [
    '// This file is auto-generated by scripts/generate-importer-version.mjs.',
    '// It fingerprints the built-in melody import pipeline for cache invalidation.',
    `export const IMPORTER_CACHE_SCHEMA_VERSION = '${version}';`,
    '',
  ].join('\n');
}

async function main() {
  const version = await buildImporterVersion();
  const nextContent = buildOutput(version);
  await mkdir(path.dirname(outputPath), { recursive: true });

  let previousContent = null;
  try {
    previousContent = await readFile(outputPath, 'utf8');
  } catch {
    previousContent = null;
  }

  if (previousContent === nextContent) {
    console.log(`Importer cache version unchanged: ${version}`);
    return;
  }

  await writeFile(outputPath, nextContent, 'utf8');
  console.log(`Updated importer cache version: ${version}`);
}

main().catch((error) => {
  console.error('Failed to generate importer cache version:', error);
  process.exitCode = 1;
});
